#include "libft.h"

void	ft_striter(char *s, void (*f)(char*))
{
	unsigned int i;

	i = 0;
	if (s == NULL || f == NULL)
		return ;
	if (s && f)
	{
		while (s[i])
		{
			(*f)(&s[i]);
			i++;
		}
	}
}
